void SDCard_Show_Info(void)
{
    uint64_t card_capacity;
    HAL_SD_CardInfoTypeDef sd_card_info;
    HAL_SD_CardCIDTypeDef sd_card_cid;

    HAL_SD_GetCardCID(&hsd1, &sd_card_cid);
    HAL_SD_GetCardInfo(&hsd1,&sd_card_info);

    switch (sd_card_info.CardType)
    {
        case CARD_SDSC:
        {
            if (sd_card_info.CardVersion == CARD_V1_X)
            {
                printf("Card Type:SDSC V1\r\n");
            }
            else if (sd_card_info.CardVersion == CARD_V2_X)
            {
                printf("Card Type:SDSC V2\r\n");
            }
        }
            break;

        case CARD_SDHC_SDXC:
            printf("Card Type:SDHC\r\n");
            break;
    }

    card_capacity = (uint64_t)(sd_card_info.LogBlockNbr) * (uint64_t)(sd_card_info.LogBlockSize); /* ??SD??? */
    printf("Card ManufacturerID:%d\r\n", sd_card_cid.ManufacturerID);                /* ???ID */
    printf("Card RCA:%d\r\n", sd_card_info.RelCardAdd);                     /* ????? */
    printf("LogBlockNbr:%d \r\n", (uint32_t)(sd_card_info.LogBlockNbr));    /* ??????? */
    printf("LogBlockSize:%d \r\n", (uint32_t)(sd_card_info.LogBlockSize));  /* ??????? */
    printf("Card Capacity:%d MB\r\n", (uint32_t)(card_capacity >> 20));              /* ???? */
    printf("Card BlockSize:%d\r\n\r\n", sd_card_info.BlockSize);            /* ????? */
}
